
# ANSI escape codes for standard text colors
BLACK = "\033[30m"
RED = "\033[31m"
GREEN = "\033[32m"
YELLOW = "\033[33m"
BLUE = "\033[34m"
MAGENTA = "\033[35m"
CYAN = "\033[36m"
WHITE = "\033[37m"

# ANSI escape codes for light (bright) text colors
LIGHTBLACK_EX = "\033[90m"  # Bright Black (Gray)
LIGHTRED_EX = "\033[91m"    # Light Red
LIGHTGREEN_EX = "\033[92m"  # Light Green
LIGHTYELLOW_EX = "\033[93m" # Light Yellow
LIGHTBLUE_EX = "\033[94m"   # Light Blue
LIGHTMAGENTA_EX = "\033[95m" # Light Magenta
LIGHTCYAN_EX = "\033[96m"   # Light Cyan
LIGHTWHITE_EX = "\033[97m"  # Light White (Bright White)

RESET = "\033[0m"  # Reset to default color
